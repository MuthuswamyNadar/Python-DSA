# list in arrays (can be modified (mutuable))

my_list=[1,2,3,"hello",5]
print(my_list[3])

print(my_list[4])

# Tuple cannot be modified (immutuable)

mytuple=(1,2,3)
print(mytuple[1])

#Dictionaries

my_dic={"name:20","age:Twenty","Hate: age"}

print(my_dic)

# sets (where the duplicate will be removed )
my_set={1,2,2,3,4,5,5,5}
print(my_set)















